#include "dog.h"

/**
 * init_dog - initialize a variable of type struct dog
 * @dg: struct dog
 * @name: Name of dog
 * @age: Age of the dog
 * @owner: Owner of the dog
 *
 * Return: No return
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
	if (dg)
	{
		dg->name = name;
		dg->age = age;
		dg->owner = owner;
	}
}

