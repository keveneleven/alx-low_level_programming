#include <string.h>
/**
 * _memset - fills memory with a constant byte
 * @s: memory area
 * @b: address of memory to print
 *
 * Return: a pointer to the memory area s
 */
char *_memset(char *s, char b, unsigned int n);
{
	unsigned int m;
	
	for (int m = 0; m < 0; m++)
		*(s + m) = b;
	return (s);
}

